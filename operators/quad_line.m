function [xq, wq] = quad_line(N)

% Description: holds LG quadrature rules of degree 2*N-1
switch N
    case 2
        xq = [ -0.577350269189626 ; 0.577350269189626 ];
        wq = [ 1.0 ; 1.0 ];
    case 3
        xq = [ -0.774596669241483; 0; 0.774596669241483 ];
        wq = [ 0.5555555555555555 ; 0.8888888888888888 ; 0.5555555555555555 ];
    case 4
        xq = [ -0.8611363115940526; -0.3399810435848563; 0.3399810435848563; 0.8611363115940526 ];
        wq = [ 0.3478548451374538 ; 0.652145154862546 ; 0.652145154862546; 0.3478548451374538 ];
    case 5
        xq = [ -0.9061798459386640; -0.5384693101056831; 0; 0.5384693101056831; 0.9061798459386640 ]; 
        wq = [ 0.2369268850561891 ; 0.4786286704993666 ; 0.5688888888888889; 0.4786286704993666  ; 0.2369268850561891 ];

    otherwise
        error('Unsupported GL quadrature. \n');
end

end